            <h2>Perfumes, perfumed or eau de toilette?</h2>
            <p>What is actually on your table?</p>
            <img data-src="https://via.placeholder.com/1140x500" src="data:image/gif;base64,R0lGODlhAQABAAAAACw=" class="js-img"
                alt="">
            <ul class="post-top__info">
