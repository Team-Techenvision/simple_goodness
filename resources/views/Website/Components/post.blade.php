        <div class="post-content">
            <p>The history of perfume is about 5,000 years old and dates back to ancient Egypt and
                Mesopotamia. The fragrances of those times differed significantly from modern ones in that
                they were very concentrated and were more often intended for religious ceremonies than for
                cosmetic purposes. Although the same Cleopatra had her own perfume factory and left behind a
                recipe for a fragrance of 16 ingredients - "kifi". It is difficult to call it a perfume in
                the modern sense - it is rather a mixture of oils and herbs. Modern perfumes can have a
                maximum of 30% aromatic component, everything else - an alcoholic solution. But do not be
                upset. If you do not need such extravagant appearances as in the queen of Egypt, when the
                fragrances of her ships spread throughout the coast, then 100% concentrate is unlikely to be
                needed.</p>
            <h6>About sustainability</h6>
            <p>It would seem that everything is clear here: the higher the concentration of aromatic
                components - the more stable the aroma. But not everything is so simple: even if in the
                laboratory the aroma stays calm for a day or more, in life it is influenced by humidity and
                temperature, skin condition and the aroma of your body. Therefore, experts advise to apply
                fragrances on a clean, moisturizing body lotion. But even this does not guarantee that
                super-concentrated perfumes will last longer than perfumed or eau de toilette. It all
                depends on the volatility of the ingredients themselves.
            <p>
            <blockquote class="blockquote">
                “Fruity notes are considered the lightest, but sandalwood and amber - the strongest. This
                does not mean that fruit flavors should be forgotten. First, they have a good plume, and
                secondly, history knows many cases where the aroma was much more stable than required by the
                situation.”
                <span class="blockquote-author">Melissa Jones</span>
            </blockquote>
            <ul class="post-list">
                <li>
                    <span>Perfume</span>
                    Is the most expensive type of perfume, because it contains the highest concentration of
                    aromatic composition - 20-30%. Therefore, they should be used only for special events.
                </li>
                <li>
                    <span>Perfumed water</span>
                    Is the most concentrated after perfumes mixture of perfume composition 12-15% with ethyl
                    alcohol 85%.
                </li>
                <li>
                    <span>Cologne</span>
                    is the least concentrated type of perfume. Contains 5% aromatic substances dissolved in
                    80% ethyl alcohol. By the way, it was the most famous French emperor who introduced
                    colognes into fashion and used 1-2 bottles a day
                </li>
                <li>
                    <span>Eau de toilette</span>
                    is what is most likely on your cosmetic table. The most common type of perfume. Contains
                    6-12% of the perfume composition. The term itself, by the way, was invented by Napoleon
                    when he invented a bergamot-based fragrance recipe during a military campaign and called
                    it "eau de toilette."
                </li>
            </ul>
